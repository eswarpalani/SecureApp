package com.virtusa.virtusabank.secureapp.model;

public class StatementInfo {

	Integer fromAccId;
	Long from;
	Long to;

	public Integer getFromAccId() {
		return fromAccId;
	}

	public void setFromAccId(Integer fromAccId) {
		this.fromAccId = fromAccId;
	}

	public Long getFrom() {
		return from;
	}

	public void setFrom(Long from) {
		this.from = from;
	}

	public Long getTo() {
		return to;
	}

	public void setTo(Long to) {
		this.to = to;
	}

}
